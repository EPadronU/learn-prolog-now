% Modules ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
:- ['readWord'].
% ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~


% ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
read_file(Stream) :-
  readWord(Stream, Word),
  \+ (Word = ''),
  assertz(word(Word)),
  read_file(Stream).

read_file(_) :- !.


main :-
  open('test_file.txt', read, Stream),
  read_file(Stream),
  close(Stream), !.
